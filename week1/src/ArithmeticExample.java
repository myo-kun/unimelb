public class ArithmeticExample {
    public static void main(String[] args) {
        float a = 20.5F, b = 6.4F;
        int c = 11, d = 5;
        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));
        System.out.println("a*b = " + (a*b));
        System.out.println("a/b = " + (a/b));
        System.out.println("c%d = " + (c%d));
    }
}
