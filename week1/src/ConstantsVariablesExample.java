public class ConstantsVariablesExample {
    public static void main(String[] args) {
        // constant
        final double PI = 3.1428;
        // local variable
        float radius =5.5F;
        double area;
        boolean x = true;
        area = PI * radius * radius;
        System.out.println("Circle Radius = " + radius + " Area = " + area);
        System.out.println("Boolean Value = " + x);
    }
}
